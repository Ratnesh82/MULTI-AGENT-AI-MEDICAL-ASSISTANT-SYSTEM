def _send_sync(to_email: str, subject: str, html_body: str) -> dict:
    """Synchronous SMTP send — called via run_in_executor."""
    if not SMTP_HOST or not SMTP_USER or not SMTP_PASSWORD:
        print(f"\n📧 [EMAIL MOCK] To: {to_email} | {subject}")
        print("   → Add SMTP_HOST/SMTP_USER/SMTP_PASSWORD to .env for real delivery\n")
        return {"sent": True, "method": "console_mock"}

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = FROM_EMAIL
    msg["To"]      = to_email
    msg.attach(MIMEText(html_body, "html"))

    try:
        with smtplib.SMTP(SMTP_HOST, SMTP_PORT, timeout=15) as server:
            server.ehlo()
            server.starttls()
            server.login(SMTP_USER, SMTP_PASSWORD)
            server.sendmail(FROM_EMAIL, to_email, msg.as_string())
        print(f"📧 [EMAIL SENT] To: {to_email} | {subject}")
        return {"sent": True, "method": "smtp"}
    except Exception as e:
        print(f"📧 [EMAIL ERROR] {e}")
        return {"sent": False, "error": str(e)}


async def send_appointment_email(
    to_email: str,
    patient_name: str,
    doctor_name: str,
    specialty: str,
    slot_time: str,
    appointment_id: str,
) -> dict:
    """Send appointment confirmation — SMTP runs in thread pool."""
    if not to_email:
        return {"sent": False, "reason": "no email address"}

    slot_str_short = ""
    if slot_time:
        try:
            slot_str_short = f" on {datetime.fromisoformat(slot_time).strftime('%d %b')}"
        except Exception:
            pass

    subject = f"✅ Appointment Confirmed — {doctor_name}{slot_str_short}"
    html    = _build_confirmation_html(patient_name, doctor_name, specialty, slot_time, appointment_id)

    loop = asyncio.get_event_loop()
    return await loop.run_in_executor(None, _send_sync, to_email, subject, html)


