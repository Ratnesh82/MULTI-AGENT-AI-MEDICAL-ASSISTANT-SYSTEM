"""
Email Notification Service — Phase 3 (fixed).
Reads SMTP settings from config.py (which reads from .env).
Uses run_in_executor so smtplib never blocks the async event loop.
"""
import smtplib
import asyncio
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from datetime import datetime
from config import settings

SMTP_HOST     = settings.smtp_host
SMTP_PORT     = settings.smtp_port
SMTP_USER     = settings.smtp_user
SMTP_PASSWORD = settings.smtp_password
FROM_EMAIL    = settings.from_email or "noreply@fcmas-w.health"


def _fmt_slot(slot_time: str) -> str:
    """Safely format a slot_time ISO string to human-readable."""
    if not slot_time:
        return "TBD"
    try:
        return datetime.fromisoformat(slot_time).strftime("%A, %d %B %Y at %I:%M %p")
    except Exception:
        return slot_time


